    <h3>Pourquoi consulter?</h3>
    <p>Franchir le pas pour consulter un psychologue n'est pas toujours facile à réaliser.
        Au cours d'une vie, il existe de nombreuses raisons de rencontrer un psychologue qui sont toujours
        uniques et propres à chacun. Celles-ci vont donner naissance à une demande, permettre une
        rencontre et ainsi donner lieu à un soutien ponctuel ou à une psychothérapie plus approfondie.
        Ces demandes peuvent être de différentes natures, allant du besoin de faire le point ponctuellement
        sur sa vie, de soulager un mal-être ou de se libérer d’un poids inconfortable qui empêche
        l'épanouissement total.
        Que ce soit pour des problématiques de gestion émotionnelle, de conflits familiaux ou relationnels,
        de maladies, de deuils, de séparations, de stress, d'anxiété, de dépression... chaque événement de
        vie, difficile à traverser seul, peut être un motif de consultation.
        Et cela concerne tous les âges de la vie.</p>


